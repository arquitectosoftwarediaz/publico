package com.example.hexagonal.dominio.modelo;

public class Area {
	
	private Long idArea;
	private String nombre;

	public Area() {
	}

	public Long getIdArea() {
		return idArea;
	}

	public void setIdArea(Long idArea) {
		this.idArea = idArea;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}



}
